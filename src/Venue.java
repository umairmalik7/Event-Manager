class Venue {
    int venueID;
    String venueName;
    String venueAddress;
    int venueCapacity;

    public Venue(int venueID, String venueName, String venueAddress, int venueCapacity) {
        this.venueID = venueID;
        this.venueName = venueName;
        this.venueAddress = venueAddress;
        this.venueCapacity = venueCapacity;
    }
}